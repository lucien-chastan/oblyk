            <div class="row">
                <h2 class="loved-king-font" style="font-size: 2em;">Comment bien commencer la création de vos topos</h2>
                <p class="text-bold">Étape 1: Créer vos échelles de difficulté</p>
                <p>
                    Votre salle fonctionne certainement avec des couleurs de prise ou d'étiquette pour indiquer la difficulté des lignes.<br>
                    Pour chaque niveau nous vous demanderons une couleur (ou deux) et la côte la plus fréquente dans cette difficulté. Ça sera plus rapide pour vous ensuite de renseigner vos lignes<br>
                </p>
                <p class="text-italic grey-text">Dans le menu de gauche : Topo > Système de cotation </p>

                <p class="text-bold">Étape 2 : Créer vos espaces</p>
                <p>
                    Un espace et une partie de votre salle (ou de vos salles), pour un espace vous aurez un plan.<br>
                    S'il est simple pour vous de découper votre salle en deux plans, alors créer deux espaces (par exemple : espace voie, espace bloc, espace enfant, etc.
                </p>
                <p class="text-italic grey-text">Dans le menu de gauche : Topo > Les espaces</p>

                <p class="text-bold">Étape 3 : Importer votre plan</p>
                <p>
                    Une fois que vous avez créer vos espaces, rendez-vous dans l'un de <cite>(à partir de maintenant toutes les étapes que nous décrirons seront à faire pour chaque espace)</cite><br>
                </p>
                <p class="text-italic grey-text">
                    Dans le menu de gauche : Topo > Les espaces > cliquez sur un espace
                </p>
                <p>
                    Une fois dans votre espace, importer l'image de fond qui fera le plan de votre topo<br>
                    Le plan peut faire max : 2000px * 2000px
                </p>

                <p class="text-bold">Étpae 4 : Créer les secteurs</p>
                <p>
                    Un secteur est une zone identifiable de votre espace, comme un couloir de voie, la partie toit ou dalle de votre bloc, etc.<br>
                    Dans un espace, cliquez sur "Créer un secteur"<br>
                    Une fois le secteur créé vous pouvez dessiner la zone sur le plan et télécharger une image représentative de votre secteur.<br>
                    Pour ça : cliquez sur le secteur que vous venez de créer, en bas vous trouverez différents bouton pour paramétrer votre secteur.
                </p>

                <p class="text-bold">Étape 5 : Créer les voies</p>
                <p>
                    Allez sur un secteur, cliquez sur "Ajouter une voie". Renseignez ce qui est demandé et validez.<br>
                    Comme pour les secteurs vous pouvez télécharger une photo global de votre voie et/ou une miniature (une miniature peut être une vue proche d'une prise représentative de la voie)
                </p>
